            $theDesc = $this->createElementNS( 'http://www.tei-c.org/ns/1.0', 'desc' );
            $oDesc = $oElementSpec->appendChild( $theDesc );
	    $oDesc->appendChild( new domText( stripslashes( $aszConfig[ 'description' ] ) ) );

